public class Ex9 {
  public static void main(String[] args) {
    
    // create variables
    int a = 100;
    int b = 200;
    System.out.println("var using =: " + a);
    a += b;
    System.out.println("var using +=: " + a);
    a -= b;
    System.out.println("var using -=: " + a);
    a *= b;
    System.out.println("var using *=: " + a);
    a /= b;
    System.out.println("var using /=: " + a);
    a %= b;
    System.out.println("var using %=: " + a);
  }
}
